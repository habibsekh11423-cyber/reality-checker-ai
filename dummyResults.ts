export function generateDummyResult(
  type: "text" | "image" | "url",
  input: string
): Omit<ScanResult, "id" | "scannedAt"> {
  const rand = seededRand(hashStr(input + type));

  const rawScore = rand();
  let verdict: Verdict;
  let confidence: number;

  if (rawScore < 0.35) {
    verdict = "likely-fake";
    confidence = Math.round(72 + rand() * 22);
  } else if (rawScore < 0.6) {
    verdict = "suspicious";
    confidence = Math.round(55 + rand() * 30);
  } else {
    verdict = "authentic";
    confidence = Math.round(78 + rand() * 20);
  }

  const signalDefs =
    type === "text"
      ? TEXT_SIGNALS
      : type === "image"
        ? IMAGE_SIGNALS
        : URL_SIGNALS;

  const signals: Signal[] = signalDefs.map((def) => {
    const base = rand();
    let score: number;
    let flagged: boolean;

    if (verdict === "authentic") {
      score = Math.round(70 + base * 28);
      flagged = score < 78;
    } else if (verdict === "suspicious") {
      score = Math.round(40 + base * 45);
      flagged = score < 65;
    } else {
      score = Math.round(15 + base * 45);
      flagged = score < 60;
    }

    return {
      label: def.label,
      score,
      description: def.desc,
      flagged,
    };
  });

  const summaryArr =
    type === "text"
      ? TEXT_SUMMARIES[verdict]
      : type === "image"
        ? IMAGE_SUMMARIES[verdict]
        : URL_SUMMARIES[verdict];

  const summaryIdx = Math.floor(rand() * summaryArr.length);
  const summary = summaryArr[summaryIdx];

  return { type, input, verdict, confidence, signals, summary };
}
